#include "libft.h"

size_t	ft_lenint(long long n)
{
	int	len;

	len = 1;
	if (n < 0)
	{
		if (n == -2147483648)
			n = n + 1;
		n = n * -1;
	}
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
